"""
Semantic comparison of conda packages.

This module compares two conda packages - typically one generated by
whl2conda from a PyPI wheel and a reference package built from a recipe
(e.g. by conda-build or rattler-build for conda-forge) - and reports
their differences, classifying each as expected (benign differences
inherent to the different build processes), notable, or unexpected
errors.
"""

